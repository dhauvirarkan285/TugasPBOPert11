    private void loadData() {
    model = new DefaultTableModel();
    model.addColumn("ID Armada");
    model.addColumn("Nomor Armada");
    model.addColumn("Jenis Armada");
    model.addColumn("Tanggal Mulai Dinas");
    model.addColumn("Perawatan Terakhir");
    model.addColumn("Perawatan Berikutnya");

    List<Depokeretasbi> list = em.createQuery("SELECT d FROM Depokeretasbi d", Depokeretasbi.class).getResultList();
    for (Depokeretasbi d : list) {
        model.addRow(new Object[]{
            d.getIdArmada(),
            d.getNomorArmada(),
            d.getJenisArmada(),
            d.getTanggalMulaiDinas(),
            d.getTanggalPerawatanTerakhir(),
            d.getTanggalPerawatanBerikutnya()
        });
    }

    jTable1.setModel(model);
}

