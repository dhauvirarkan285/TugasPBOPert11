    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String nomor = JOptionPane.showInputDialog("Nomor Armada:");
            String jenis = JOptionPane.showInputDialog("Jenis Armada:");
            if (nomor == null || jenis == null || nomor.isEmpty() || jenis.isEmpty()) return;

            Date tglMulai = new java.sql.Date(System.currentTimeMillis());
            em.getTransaction().begin();
            Depokeretasbi d = new Depokeretasbi();
            d.setNomorArmada(nomor);
            d.setJenisArmada(jenis);
            d.setTanggalMulaiDinas(tglMulai);
            em.persist(d);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
            loadData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal insert: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row >= 0) {
            Integer id = (Integer) jTable1.getValueAt(row, 0);
            Depokeretasbi d = em.find(Depokeretasbi.class, id);
            if (d != null) {
                String jenisBaru = JOptionPane.showInputDialog("Jenis baru:", d.getJenisArmada());
                if (jenisBaru != null && !jenisBaru.isEmpty()) {
                    em.getTransaction().begin();
                    d.setJenisArmada(jenisBaru);
                    em.getTransaction().commit();
                    JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
                    loadData();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin diupdate.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row >= 0) {
            Integer id = (Integer) jTable1.getValueAt(row, 0);
            Depokeretasbi d = em.find(Depokeretasbi.class, id);
            if (d != null) {
                int confirm = JOptionPane.showConfirmDialog(this, "Yakin hapus data ini?");
                if (confirm == JOptionPane.YES_OPTION) {
                    em.getTransaction().begin();
                    em.remove(d);
                    em.getTransaction().commit();
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                    loadData();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
        Connection conn = DriverManager.getConnection(
            "jdbc:postgresql://localhost:5432/pbo_pert7", 
            "postgres", 
            "ABCDE12345"
        );

        // Pastikan path benar (ambil dari resource)
        String jrxmlPath = getClass().getResource("/tugaspert11/DataKereta.jrxml").getPath();
        String jasperPath = jrxmlPath.replace(".jrxml", ".jasper");

        // Compile ulang setiap kali tombol ditekan agar sinkron
        System.out.println("Mengompilasi ulang file JRXML ke JASPER...");
        JasperCompileManager.compileReportToFile(jrxmlPath, jasperPath);

        Map<String, Object> params = new HashMap<>();
        params.put("tanggalLaporan", new java.util.Date());

        JasperPrint print = JasperFillManager.fillReport(jasperPath, params, conn);
        JasperViewer.viewReport(print, false);
        conn.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal mencetak laporan: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton4ActionPerformed

